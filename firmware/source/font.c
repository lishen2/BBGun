void font_getdot(font_type font, char *code, uint8_t *dot)
{
    uint8_t idx;
    uint32_t dotlen;
    uint32_t offset;
    uint8_t *fontptr;

    idx = *code;
    if (idx < 32 || idx > 126){
        return;
    }    

    switch(font)
    {
    case FONT_ASCII_6X8:
        dotlen = 8;
        break;
    case FONT_ASCII_8X16:
        dotlen = 16;
        break;
    default:
        dotlen = 0;
        break;
    };
    if (0 == dotlen){
        return;
    }
    
    switch(font)
    {
    case FONT_ASCII_6X8:
        fontptr = ASCFont6X8;
        break;
    case FONT_ASCII_8X16:
        fontptr = ASCFont8X16;
        break;
    default:
        fontptr = NULL;
        break;
    };
    if (NULL == fontptr){
        return;
    }

    idx -= 32;
    offset = idx * dotlen;
    memcpy(dot, fontptr + offset, dotlen);

    return;
}

